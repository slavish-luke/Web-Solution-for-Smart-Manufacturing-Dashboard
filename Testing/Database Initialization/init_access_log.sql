DROP TABLE IF EXISTS access_log;

CREATE TABLE access_log (
    id int NOT NULL AUTO_INCREMENT,
    timestamp datetime,
    user_id INT,
    role int(5),
    PRIMARY KEY (id),
    FOREIGN KEY (user_id) REFERENCES account(id),
    FOREIGN KEY (role) REFERENCES role(id)
);

